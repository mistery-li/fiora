<template>
    <div>App</div>
</template>

<script lang="ts">
import { defineComponent } from 'vue';
import { isMobile } from '../utils/ua';

export default defineComponent({
    name: 'App',
    components: {},
    setup() {
        /**
         * 获取窗口宽度百分比
         */
        function getWidthPercent() {
            let width = 0.6;
            if (isMobile) {
                width = 1;
            } else if (window.innerWidth < 1000) {
                width = 0.9;
            } else if (window.innerWidth < 1300) {
                width = 0.8;
            } else if (window.innerWidth < 1600) {
                width = 0.7;
            } else {
                width = 0.6;
            }
            return width;
        }

        /**
         * 获取窗口高度百分比
         */
        function getHeightPercent() {
            let height = 0.8;
            if (isMobile) {
                height = 1;
            } else if (window.innerHeight < 1000) {
                height = 0.9;
            } else {
                height = 0.8;
            }
            return height;
        }

        return {
            getWidthPercent,
        };
    },
});
</script>

